import json


def build_daily_short_prompt(filename: str, pdf_text: str) -> str:
    return f"""
[프롬프트]
당신은 국내 50대 대기업 CEO이자 국제 정세학 박사입니다.
CEO님의 관점에서 볼때 해당 보고서의 주요 핵심 내용을 국문으로 2줄로 간결히 요약해주세요.
수치 변동은 괄호 안에 화살표(->)로 알려주시고 그전의 데이터를 학습에 사용하지 마십시오.
요약시 단어로 끝나도록 해주고(예: ~리스크 다발, ~불확실성 잔존 등) C-LEVEL 톤으로 써주세요.
직함 병기 필수 (예: 젤렌스키 대통령, 다카이치 총리)

[절대 규칙]
- 입력된 PDF 원문에 있는 내용만 사용할 것
- 외부 정보, 추측, 의견을 절대 넣지 말 것
- 반드시 markdown 코드블록 없이 순수 JSON만 출력할 것

[출력 JSON 형식]
{{
  "file_name": "{filename}",
  "daily_two_line_summary": [
    "첫 번째 2줄 요약",
    "두 번째 2줄 요약"
  ]
}}

[원문]
{pdf_text}
""".strip()


def build_daily_structured_prompt(filename: str, pdf_text: str) -> str:
    return f"""
당신은 글로벌 정세 분석 전문가입니다.
Eurasia Group의 Daily Brief 원문만을 기반으로, 주간 보고서 생성을 위한 구조화 데이터를 추출하세요.

[수신 대상]
- 임원진(C-Level)
- 해당 동향을 바탕으로 담당 고객사(국내 50대 대기업)에 미치는 영향을 판단하는 데 활용

[절대 규칙]
- 입력된 PDF 원문에 명시적으로 기재된 내용만 사용할 것
- 외부 정보, 추측, 의견을 절대 포함하지 말 것
- 원문에 없는 수치·사실·인과관계를 생성하지 말 것
- 출처 없는 항목은 작성하지 말 것
- 반드시 markdown 코드블록 없이 순수 JSON만 출력할 것

[해야 할 일]
1. 문서 전체를 읽고 국가/지역별 주요 동향을 분류
2. 국내 50대 대기업에 실질적 영향이 있는 이슈를 우선 선별
3. 주간 단계에서 같은 이슈를 묶을 수 있도록 topic_key를 일관되게 작성
4. 국가/지역은 예: 미국, 일본, 중국, 유럽, 인도, 중동, 러시아·우크라이나, 중남미, ASEAN 등으로 정리
5. 하나의 문서에서 regional_items는 너무 많지 않게, 핵심 위주로 추출

[topic_key 작성 규칙]
- 영어 snake_case
- 같은 주제는 가능한 한 같은 키 사용
- 예: us_china_trade, middle_east_oil_risk, japan_fiscal_risk, china_export_controls

[출력 JSON 형식]
{{
  "file_name": "{filename}",
  "document_summary": "문서 전체 3~5줄 요약",
  "regional_items": [
    {{
      "region": "미국",
      "topic_key": "us_china_trade",
      "headline": "핵심 한 줄",
      "detail": "세부 내용",
      "implication": "국내 50대 대기업 영향. 없으면 빈 문자열",
      "citations": [
        {{
          "quote": "원문 문장 그대로",
          "source": "{filename}"
        }}
      ]
    }}
  ]
}}

[원문]
{pdf_text}
""".strip()


def build_weekly_final_prompt(week_start: str, week_end: str, weekly_input_data: dict) -> str:
    data_str = json.dumps(weekly_input_data, ensure_ascii=False, indent=2)

    return f"""
당신은 글로벌 정세 분석 전문가입니다.
매일 받아보는 EG Group의 리포트를 바탕으로 주간 글로벌 정세 동향 주요 내용을 큐레이션해 요약 보고서를 생성합니다.

아래 [입력]을 읽고, 반드시 아래 두 개 섹션을 모두 작성하세요.

1) [주요 아젠다 별 한국 기업에의 영향]
2) [국가 및 권역별 주요 동향 - 상세]

[수신 대상]
- 임원진(C-Level)
- 해당 동향을 바탕으로 담당 고객사(국내 50대 대기업)에 미치는 영향을 판단하는 데 활용

[절대 규칙]
- 입력 데이터에 포함된 정보만 사용할 것
- 외부 지식, 추측, 의견을 절대 넣지 말 것
- 원문에 없는 수치·사실·인과관계를 추가하지 말 것
- 주간 내 동일 이슈가 여러 번 등장한 경우 최신 데이터 기준으로 작성하되, 필요 시 변동 추이는 함께 표기
- markdown 코드블록은 사용하지 말 것

[섹션 1 작성 규칙]
- 제목은 정확히 [주요 아젠다 별 한국 기업에의 영향] 으로 작성
- 한국 기업/산업 영향 중심으로 5개 내외 불렛 작성
- 포맷:
  - {{아젠다}}: {{영향 1~2문장. 무엇이 어떻게 변하는지 명시. 섹터/품목 구체화}}
    (주요 기업: {{한국 대표 기업 3~5개}})
- ‘국내 제조기업’, ‘관련 업계’ 같은 포괄적 표현 금지
- 각 불렛은 최대 2문장
- 중요도 높은 순으로 정렬
- 주요 키워드나 수치에만 **BOLD** 사용
- 이해 어려운 용어·기관명·사업명은 *표시 후 바로 다음 줄에 이탤릭체 한 줄 설명 추가 가능
- 한국 대표 기업은 입력 기반 이슈와 일반적인 산업 연관성을 바탕으로 3~5개 제시 가능

[섹션 2 작성 규칙]
- 제목은 정확히 [국가 및 권역별 주요 동향 - 상세] 로 작성
- 국가/지역 단위로 구분
- 형식:
  **국가/지역명**: (해당 주 핵심 내용 한 줄 요약)

  - 세부 내용
   [출처: PDF파일명 | 원문: "원문 문장 그대로 인용"]

- 하나의 bullet이 여러 자료 흐름을 반영하더라도, 현재 입력에 포함된 최신 항목 기준으로 정리
- 출처는 입력 데이터의 citations를 그대로 반영

[문체 규칙]
- 한국어 네이티브가 읽기에 자연스럽고 매끄러운 문장 사용
- C-Level 보고에 적합한 간결하고 전문적인 톤 유지
- 번역투 표현 지양
- 인명 최초 등장 시 직함 병기

[기간]
{week_start} ~ {week_end}

[입력]
{data_str}
""".strip()